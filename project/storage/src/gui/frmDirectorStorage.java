    public void addTabelPackingLists(){
        ArrayList<PackingList> packingLists = new ArrayList<>();
        String[][] data = null;
        String[] columnNames = {"id","Статус","Район", "Размер", "Дата", "Авто"};
        PackingList packingList = new PackingList();
        packingLists = packingList.getByState(PackingListState1);
        data = new String[packingLists.size()+1][6];
        if (packingLists.size()!= 0){
            for(int i=0; i < packingLists.size();i++){
                data[i][0]=""+packingLists.get(i).getId();
                data[i][1]=packingLists.get(i).getState();
                data[i][2]=packingLists.get(i).getDistrict().getDistrict();
                data[i][3]=""+packingLists.get(i).getSize();
                data[i][4]=""+packingLists.get(i).getFirsDate();
                if(packingLists.get(i).getCar() == null){
                    data[i][5]="";
                }else{
                    data[i][5]=""+packingLists.get(i).getCar().getName();
                }
            }
        }
        TableModel model = new DefaultTableModel(data, columnNames);
        tblPackingLst.setModel(model);
    }

