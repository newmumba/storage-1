    public void addTabelOrder1(){
        orders = new ArrayList<>();
        String[][] data = null;
        String[] columnNames = {"Статус","Сумма", "Размер", "Дата", "Район" ,"Имя заказчика", "Адрес заказчика", "Телефон заказчика", "id Заказчика"};
        Order order = new Order();
        orders = order.getByState(OrderState1);
        data = new String[orders.size()+1][9];
        if (orders.size()!= 0){
            for(int i=0; i < orders.size();i++){
                data[i][0]=orders.get(i).getState();
                data[i][1]=""+orders.get(i).getAmount();
                data[i][2]=""+orders.get(i).getSize();
                data[i][3]=""+orders.get(i).getDate();
                data[i][4]=""+orders.get(i).getDistrict().getDistrict();
                data[i][5]=""+orders.get(i).getCustomer().getName();
                data[i][6]=""+orders.get(i).getCustomer().getAddress();
                data[i][7]=""+orders.get(i).getCustomer().getPhone();
                data[i][8]=""+orders.get(i).getCustomer().getId();
            }
        }
        TableModel model = new DefaultTableModel(data, columnNames);
        tblOrder1.setModel(model);
    }
    
